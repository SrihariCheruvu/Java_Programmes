package basics;
/*
 * + : addition
 * - : subtraction
 * * : multiplication
 * / : divide and give quotient as answer
 * % : divide and give remainder as the answer
 */
public class ArithmeticOperator {

	public static void main(String[] args) {
		
		int x=5;
		int y=2;
		
		System.out.println("Addition= "+(x+y));
		System.out.println("Subtraction= "+(x-y));
		System.out.println("Multiplication= "+(x*y));
		System.out.println("Divide with quotient= "+(x/y));
		System.out.println("Divide with remainder= "+(x%y));
	}

}
