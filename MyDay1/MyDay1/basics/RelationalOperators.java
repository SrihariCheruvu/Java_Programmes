package basics;
/*
 *  a > b will give true if a is greater than b 
 *  a < b  will give true if a is less than b
 *  a>= b will give true if a is greater than or equal to b
 *  <= will give true if a is less than or equal to b
 *  == will give true only if both have exact same value
 *  != will give true if both have different values
 */
public class RelationalOperators {

	public static void main(String[] args) {
		
		int a=6;
		int b=6;
		
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a!=b);

	}

}
